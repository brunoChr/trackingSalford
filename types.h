#ifndef TYPES_H
#define TYPES_H

typedef char			CHAR;	// Range: {a, z}, 1 byte.
typedef unsigned char	BOOL;	// Range: {0, 1}, 1 byte.
typedef unsigned char	BYTE;	// Range: {0, 255}, 1 byte.
typedef signed char		SHORT;	// Range: {-128, 127}, 1 byte.
typedef int				INT;	// Range: {-32,768, 32,767}, 2 bytes.
typedef long			LONG;	// Range: {-2,147,483,648, 2,147,483,647}, 4 bytes.
typedef float			FLOAT;	// Range: {1.0E-38, 1.0E+38}, 4 bytes.

#endif
